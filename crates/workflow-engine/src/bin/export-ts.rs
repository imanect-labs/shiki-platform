//! ワークフロー語彙・IR 型・ノードカタログを TypeScript へ書き出す（codegen が正・scripts/gen-api.sh から呼ぶ）。
//!
//! - `workflow-ir.ts` — IR/params/検証エラーの型（ts-rs decl の連結）。
//! - `workflow-catalog.ts` — `NODE_CATALOG` 定数（日本語ラベル・ポート・スコープ・可用性）と
//!   `NodeParamsByType` マップ型（右パネル UI の per-node フォーム契約・Task 10.6/10.12）。

#![allow(clippy::expect_used, clippy::print_stdout)]

use std::path::PathBuf;

use ts_rs::TS;
use workflow_engine::ir::edge::Edge;
use workflow_engine::ir::expr::{CmpOp, Comparison, Condition, FromRef, TemplateExpr, ValueExpr};
use workflow_engine::ir::node::{Backoff, Node, OnError, RetryPolicy};
use workflow_engine::ir::params::{
    AgentInvokeParams, BranchParams, CsvPatchParams, CsvQueryParams, CsvWriteParams, HttpMethod,
    HttpRequestParams, HttpSecretRef, JoinMode, JoinParams, LlmInvokeParams, MapItemError,
    MapParams, RagSearchParams, RedirectPolicy, ScriptRunParams, ScriptSourceSpec, SecretAttach,
    SecretAttachKind, StorageListParams, StorageReadParams, StorageWriteParams, SwitchCase,
    SwitchParams, WaitKind, WaitParams, WaitTimeout, WorkflowStartParams,
};
use workflow_engine::ir::trigger::{
    Catchup, EventTrigger, InteractiveTrigger, ScheduleTrigger, Trigger, TriggerKind,
};
use workflow_engine::ir::validate::ValidationError;
use workflow_engine::ir::{Policies, TriggerOverflow, WorkflowIr};
use workflow_engine::vocab::{
    node_catalog, EventSource, IdempotencyClass, NodeCatalogEntry, NodeCategory, NodeType,
    RunEventKind, Scope,
};

/// Stage A 実装済みノード種 → params 型名（TS の `NodeParamsByType` マップの単一定義）。
const PARAMS_BY_TYPE: &[(NodeType, &str)] = &[
    (NodeType::ControlBranch, "BranchParams"),
    (NodeType::ControlSwitch, "SwitchParams"),
    (NodeType::ControlJoin, "JoinParams"),
    (NodeType::ControlMap, "MapParams"),
    (NodeType::ControlWait, "WaitParams"),
    (NodeType::StorageRead, "StorageReadParams"),
    (NodeType::StorageWrite, "StorageWriteParams"),
    (NodeType::StorageList, "StorageListParams"),
    (NodeType::RagSearch, "RagSearchParams"),
    (NodeType::LlmInvoke, "LlmInvokeParams"),
    (NodeType::AgentInvoke, "AgentInvokeParams"),
    (NodeType::HttpRequest, "HttpRequestParams"),
    (NodeType::ScriptRun, "ScriptRunParams"),
    (NodeType::WorkflowStart, "WorkflowStartParams"),
    (NodeType::CsvQuery, "CsvQueryParams"),
    (NodeType::CsvPatch, "CsvPatchParams"),
    (NodeType::CsvWrite, "CsvWriteParams"),
];

fn main() {
    // 出力先ディレクトリ（authz-vocab.ts と同じ generated 直下・単一ファイルにまとめる）。
    let out_dir = std::env::args()
        .nth(1)
        .map_or_else(|| PathBuf::from("web/src/generated"), PathBuf::from);
    std::fs::create_dir_all(&out_dir).expect("出力ディレクトリ作成");

    // 全型を 1 ファイルへ連結する（型が相互参照するため import を挟まず同一スコープに置く）。
    let mut decls: Vec<String> = Vec::new();
    macro_rules! export {
        ($($t:ty),+ $(,)?) => {
            $( decls.push(format!("export {}", <$t as TS>::decl())); )+
        };
    }
    export!(
        Scope,
        NodeType,
        EventSource,
        RunEventKind,
        WorkflowIr,
        Policies,
        TriggerOverflow,
        Node,
        RetryPolicy,
        Backoff,
        OnError,
        Edge,
        Trigger,
        TriggerKind,
        ScheduleTrigger,
        Catchup,
        EventTrigger,
        InteractiveTrigger,
        FromRef,
        TemplateExpr,
        ValueExpr,
        Condition,
        CmpOp,
        Comparison,
        ValidationError,
        // ---- ノード params（typed 契約・Task 10.6）----
        BranchParams,
        SwitchCase,
        SwitchParams,
        JoinMode,
        JoinParams,
        MapItemError,
        MapParams,
        WaitKind,
        WaitTimeout,
        WaitParams,
        StorageReadParams,
        StorageWriteParams,
        StorageListParams,
        RagSearchParams,
        LlmInvokeParams,
        AgentInvokeParams,
        HttpMethod,
        SecretAttachKind,
        SecretAttach,
        HttpSecretRef,
        RedirectPolicy,
        HttpRequestParams,
        ScriptSourceSpec,
        ScriptRunParams,
        WorkflowStartParams,
        CsvQueryParams,
        CsvPatchParams,
        CsvWriteParams,
        // ---- カタログ ----
        NodeCategory,
        IdempotencyClass,
        NodeCatalogEntry,
    );
    let path = out_dir.join("workflow-ir.ts");
    let contents = format!(
        "// @generated by shiki-workflow-engine export-workflow-ts — 手書き禁止\n{}\n",
        decls.join("\n\n")
    );
    std::fs::write(&path, contents).expect("TS 書き出し");
    println!("workflow TS 型を {} に書き出しました", path.display());

    // ノードカタログ定数＋ params マップ型（UI パレット・右パネルの単一ソース）。
    let catalog_json = serde_json::to_string_pretty(&node_catalog()).expect("カタログ JSON 直列化");
    let params_map: Vec<String> = PARAMS_BY_TYPE
        .iter()
        .map(|(nt, ty)| format!("  \"{}\": {ty};", nt.as_str()))
        .collect();
    let catalog_path = out_dir.join("workflow-catalog.ts");
    let catalog_contents = format!(
        "// @generated by shiki-workflow-engine export-workflow-ts — 手書き禁止\n\
         import type {{\n  NodeCatalogEntry,\n{}\n}} from \"./workflow-ir\";\n\n\
         /// ノードカタログ（日本語ラベル・ポート・要求スコープ・可用性・Rust 単一定義から生成）。\n\
         export const NODE_CATALOG: readonly NodeCatalogEntry[] = {catalog_json} as const;\n\n\
         /// Stage A 実装済みノード種 → params 型（右パネルのフォーム契約）。\n\
         export type NodeParamsByType = {{\n{}\n}};\n",
        PARAMS_BY_TYPE
            .iter()
            .map(|(_, ty)| format!("  {ty},"))
            .collect::<Vec<_>>()
            .join("\n"),
        params_map.join("\n"),
    );
    std::fs::write(&catalog_path, catalog_contents).expect("カタログ TS 書き出し");
    println!(
        "ノードカタログを {} に書き出しました",
        catalog_path.display()
    );
}
