//! 認可語彙（[`authz::Relation`] / [`authz::ObjectType`]）を TypeScript へ出力する。
//!
//! 使い方: `cargo run -p shiki-authz --bin export-ts -- <出力先ディレクトリ>`
//! 既定の出力先は `web/src/generated`。生成物は commit せず、`pnpm gen:api` から再生成する。

// CLI バイナリ: 標準出力/標準エラーへの出力は正当な用途のため print 系 lint を許容する。
#![allow(clippy::print_stdout, clippy::print_stderr)]

use std::{fs, path::PathBuf};

use authz::{CapabilityScope, ObjectType, Relation};
use ts_rs::TS;

fn main() -> std::io::Result<()> {
    let out_dir = std::env::args()
        .nth(1)
        .unwrap_or_else(|| "web/src/generated".to_string());
    let out_dir = PathBuf::from(out_dir);
    fs::create_dir_all(&out_dir)?;

    use std::fmt::Write as _;
    let mut contents = String::new();
    contents.push_str(
        "// @generated by `cargo run -p shiki-authz --bin export-ts`. 編集しないこと。\n",
    );
    contents.push_str("// 認可語彙の単一ソース: crates/authz/src/vocab.rs\n\n");
    // String への writeln! は無謬（fmt::Result は常に Ok）なので結果は破棄する。
    let _ = writeln!(contents, "export {}\n", Relation::decl());
    let _ = writeln!(
        contents,
        "export {}
",
        ObjectType::decl()
    );
    let _ = writeln!(contents, "export {}", CapabilityScope::decl());

    let out_file = out_dir.join("authz-vocab.ts");
    fs::write(&out_file, contents)?;
    eprintln!("wrote {}", out_file.display());
    Ok(())
}
